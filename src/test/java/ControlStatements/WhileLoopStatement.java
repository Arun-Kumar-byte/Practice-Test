package ControlStatements;

public class WhileLoopStatement {
	public static void main(String[] args) {
		int Ipl= 1;
		while (Ipl<=20) {
			System.out.println("Ipl starts in March");

			//Indicates that Increase the IPL value by 1 after execution
			Ipl++; //------------ Output is 20 times

		// break statement - breaks after execution
			//break; //---------- Output only one time
		}
	}

}
